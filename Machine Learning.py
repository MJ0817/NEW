# 인공지능: 기계나 소프트웨어가 인간처럼 지능적인 작업을 수행할 수 있도록 하는 기술.
# Machine Learing: 데이터를 기반으로 데이터를 대표하는 방정식을 찾는 것.
# 머신러닝 프로세스 작동방식
# 데이터입력 -> 모델개발 -> 모델훈련 -> 테스트 및 분석 -> 모델 적용
# 데이터입력: 데이터 수집 및 저장
# 데이터 전처리: 수집한 데이터를 모델 학습에 적합하도록 가공 결측치 및 이상치 제거, 데이터 정규화 등
# 모델 선택: 회귀, 군집, 분류 등 다양한 모델 중에서 데이터에 적합한 모델 선택
# 모델 훈련: 선택한 모델을 학습시키는 단계로, 데이터를 입력하고 모델은 학습 데이터로부터 패턴을 학습
# 모델 평가: 평가 메트릭을 사용하여 모델의 성능을 평가

